public static class Calculadora
{
    public static float soma(float a, float b)
    {
        float conta = a + b;
        return conta;
    }
    public static float sub(float a, float b)
    {
        float conta = a - b;
        return conta;
    }
    public static float mult(float a, float b)
    {
        float conta = a * b;
        return conta;
    }
    public static float div(float a, float b)
    {
        float conta = a / b;
        return conta;
    }
}